package com.spring.po;

public class ImgInfo {
	
	private int image_id;
	private String image_path;
	private int image_min;
	private int image_now;
	
	
	public int getImage_id() {
		return image_id;
	}
	public void setImage_id(int image_id) {
		this.image_id = image_id;
	}
	public String getImage_path() {
		return image_path;
	}
	public void setImage_path(String image_path) {
		this.image_path = image_path;
	}
	public int getImage_min() {
		return image_min;
	}
	public void setImage_min(int image_min) {
		this.image_min = image_min;
	}
	public int getImage_now() {
		return image_now;
	}
	public void setImage_now(int image_now) {
		this.image_now = image_now;
	}
}
